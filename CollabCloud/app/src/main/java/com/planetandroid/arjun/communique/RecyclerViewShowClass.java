package com.planetandroid.arjun.communique;



public class RecyclerViewShowClass {


    private String date, report;

    public RecyclerViewShowClass() {
    }

    public RecyclerViewShowClass(String date, String report) {
        this.date = date;
        this.report = report;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getReport() {
        return report;
    }

    public void setReport(String report) {
        this.report = report;
    }


}

